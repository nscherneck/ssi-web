    {
      if($request->has('sort')) {
        $components = Component::orderBy($request->sort)
          ->where('manufacturer_id', $manufacturer->id)
          ->get();
      } else {
        $components = Component::orderBy('model')
          ->where('manufacturer_id', $manufacturer->id)
          ->get();
      }
      return view('manufacturers.show', compact('manufacturer', 'components'));
    }

