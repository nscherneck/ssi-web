    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'name' => 'required|unique:permissions|string|max:255',
        ]);

        $role = new Role([
            'name' => $request->input('name'),
            'guard_name' => 'web'
        ]);

        $role->save();
        flash('Success!', 'Role created.');
        return back();
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $role = Role::findOrFail($id);
        $assignedUsers = User::orderBy('last_name')->role($role->name)->get();
        $users = User::orderBy('last_name')->get();
        $attachedPermissions = $role->permissions;
        $permissions = Permission::all();
        return view('admin.roles.show', compact('role', 'assignedUsers', 'users', 'attachedPermissions', 'permissions'));
    }

